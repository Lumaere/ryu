mulShift64 :: Word64 -> Word128 -> Int32 -> Word64
mulShift64 m f@(Word128 factorHi factorLo) shift =
    let m' = fromIntegral m :: Word128
        bits0 = m' * fromIntegral factorLo :: Word128
        bits1 = m' * fromIntegral factorHi :: Word128
        sum = bits0 .>> 64 + bits1
     in fromIntegral $ sum .>> (fromIntegral $ shift - 64)

mulPow5InvDivPow2 :: Word64 -> Word64 -> Int32 -> Word64
mulPow5InvDivPow2 m q j = mulShift64 m (double_pow5_inv_split ! q) j

mulPow5DivPow2 :: Word64 -> Word64 -> Int32 -> Word64
mulPow5DivPow2 m i j = mulShift64 m (double_pow5_split ! i) j

acceptBounds :: Word64 -> Bool
acceptBounds v = v `div` 4 .&. 1 == 0

data BoundsState = BoundsState
    { _v :: Word64
    , _vu :: Word64
    , _vv :: Word64
    , _vw :: Word64
    , _lastRemovedDigit :: Word64
    , _vuIsTrailingZeros :: Bool
    , _vvIsTrailingZeros :: Bool
    } deriving Show

makeLenses ''BoundsState

clear' :: (BoundsState -> Bool) -> BoundsState -> (BoundsState, Int32)
clear' f d
  | f d = fmap ((+) 1) . clear' f $
                d & vu %~ flip div 10
                  & vv %~ flip div 10
                  & vw %~ flip div 10
                  & lastRemovedDigit .~ (d ^. vv `mod` 10)
                  & vuIsTrailingZeros .~ (d ^. vu `mod` 10 == 0)
                  & vvIsTrailingZeros .~ (d ^. lastRemovedDigit == 0)
  | otherwise = (d, 0)

clear :: BoundsState -> (BoundsState, Int32)
clear = clear' (\d -> d ^. vw `div` 10 > d ^. vu `div` 10)


d2dGT :: Int32 -> Word64 -> Word64 -> Word64 -> (BoundsState, Int32)
d2dGT e2 u v w =
    let q = toU $ max (log10pow2 (fromIntegral e2) - 1) 0
        e10 = fromIntegral $ q :: Int32
        k = double_pow5_inv_bitcount + pow5bits (toS q) - 1
        i = -e2 + fromIntegral (toS q + k) :: Int32
        vu = mulPow5InvDivPow2 u q i
        vv = mulPow5InvDivPow2 v q i
        vw = mulPow5InvDivPow2 w q i
        (vvIsTrailingZeros, vuIsTrailingZeros, vw') =
            case () of
              _ | q <= 21 && v `mod` 5 == 0 -> (multipleOfPowerOf5 v q, False, vw)
                | q <= 21 && acceptBounds v -> (False, multipleOfPowerOf5 u q, vw)
                | q <= 21                   -> (False, False, vw - asWord (multipleOfPowerOf5 w q))
                | otherwise                 -> (False, False, vw)
     in (BoundsState v vu vv vw' 0 vuIsTrailingZeros vvIsTrailingZeros, e10)

d2dLT :: Int32 -> Word64 -> Word64 -> Word64 -> (BoundsState, Int32)
d2dLT e2 u v w =
    let q = toU $ max (log10pow5 (fromIntegral $ -e2) - 1) 0
        e10 = fromIntegral q + e2 :: Int32
        i = -e2 - fromIntegral q
        k = pow5bits i - double_pow5_bitcount
        j = fromIntegral q - k
        vu = mulPow5DivPow2 u (fromIntegral i) j
        vv = mulPow5DivPow2 v (fromIntegral i) j
        vw = mulPow5DivPow2 w (fromIntegral i) j
        (vvIsTrailingZeros, vuIsTrailingZeros, vw') =
            case () of
              _ | q <= 1 && acceptBounds v -> (True, w - v == 2, vw) -- mmShift == 1
                | q <= 1                   -> (True, False, vw - 1)
                | q < 63                   -> (multipleOfPowerOf2 v (q - 1), False, vw)
                | otherwise                -> (False, False, vw)
     in (BoundsState v vu vv vw' 0 vuIsTrailingZeros vvIsTrailingZeros, e10)

calculate :: BoundsState -> (Word64, Int32)
calculate d
  = let (d', r) = if d ^. vuIsTrailingZeros
                     then clear' (\d -> d ^. vu `mod` 10 == 0) d
                     else (d, 0)
        d'' = if d' ^. vvIsTrailingZeros && d' ^. lastRemovedDigit == 5 && d' ^. vv `mod` 2 == 0
                 then d' & lastRemovedDigit .~ 4
                 else d'
        roundUp = _vv d'' == _vu d'' && (not (acceptBounds $ _v d'') || not (_vuIsTrailingZeros d''))
                    || _lastRemovedDigit d'' >= 5
     in (_vv d'' + asWord roundUp, r)

d2d :: Word64 -> Word32 -> FloatingDecimal
d2d m e =
    let mf = if e == 0
                then m
                else (1 .<< double_mantissa_bits) .|. m
        ef = if e == 0
                then toS 1 - toS double_bias - toS double_mantissa_bits
                else fromIntegral e - toS double_bias - toS double_mantissa_bits
        e2 = fromIntegral ef - 2 :: Int32
        -- Step 2. 3-tuple (u, v, w) * 2**e2
        u = 4 * mf - 1 - asWord (m /= 0 || e <= 1)
        v = 4 * mf
        w = 4 * mf + 2
        -- Step 3. convert to decimal power base
        (state, e10) =
            if e2 >= 0
               then d2dGT e2 u v w
               else d2dLT e2 u v w
        -- Step 4: Find the shortest decimal representation in the interval of
        -- valid representations.
        (state', removed) = clear state
        general = _vvIsTrailingZeros state || _vuIsTrailingZeros state
        reset = if general then id else const False
        -- TODO: common-case optimization for removing 2 digits if not general
        (output, removed') = calculate $ state' & (vvIsTrailingZeros %~ reset)
                                                & (vuIsTrailingZeros %~ reset)
        exp = e10 + removed + removed'
     in FloatingDecimal output exp

d2s :: Double -> String
d2s d = let bits = coerceToWord d :: Word64
            sign = ((bits .>> (double_mantissa_bits + double_exponent_bits)) .&. 1) /= 0
            mantissa = bits .&. mask double_mantissa_bits
            exponent = (bits .>> double_mantissa_bits) .&. mask double_exponent_bits
         in if (exponent == mask double_exponent_bits) || (exponent == 0 && mantissa == 0)
               then special sign (exponent > 0) (mantissa > 0)
               else let v = unifySmallTrailing <$> d2dSmallInt mantissa (fromIntegral exponent)
                        FloatingDecimal m e = fromMaybe (d2d mantissa (fromIntegral exponent)) v
                     in prependIf sign '-' $ toChars m e

